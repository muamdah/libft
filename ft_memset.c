#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	size_t i;

	i = 0;
	if (n == 0)
		return (s);
	while (i < n)
	{
		(*(unsigned char*)s) = c;
		s++;
		i++;
	}
	return (s - i);
}
