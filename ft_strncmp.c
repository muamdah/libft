#include "libft.h"

int		ft_strncmp(const char *s1, const char *s2, size_t n)
{
	size_t i;
	size_t a;

	i = 0;
	a = 0;
	while ((s1[i] != '\0' || s2[i] != '\0') && (a < n || i < n))
	{
		if ((s1[i] > s2[a]) || (s1[a] < s2[a]))
			return (s1[i] - s2[a]);
		i++;
		a++;
	}
	return (0);
}
