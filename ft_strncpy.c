#include "libft.h"

char	*ft_strncpy(char *dest, const char *src, unsigned int len)
{
	unsigned int i;

	i = 0;
	while (src[i] != '\0' && i < len)
	{
		dest[i] = src[i];
		i++;
	}
	while (dest[i])
		dest[i++] = '\0';
	return (dest);
}
