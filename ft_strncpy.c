#include "libft.h"

char	*ft_strncpy(char *dest, const char *src, unsigned int len)
{
	unsigned int i;

	i = 0;
	while (i < len && src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	while (i < len)
	{
		dest[i] = '\0';
		i++;
	}
	return (dest);
}
