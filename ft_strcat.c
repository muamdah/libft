#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int i;
	int a;

	i = 0;
	a = 0;
	while (s1[i])
		i++;
	while (s2[a])
		s1[i++] = s2[a++];
	s1[i] = '\0';
	return (s1);
}
